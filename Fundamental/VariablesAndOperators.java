public class VariablesAndOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int sum = a + b;
        int diff = a - b;
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + diff);

        boolean flag = (a > b);
        System.out.println("Is a greater than b? " + flag);
    }
}
